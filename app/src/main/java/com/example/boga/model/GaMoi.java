package com.example.boga.model;

public class GaMoi {
    int id;
    String tengamoi;
    String giaga;

    String hinhanh;
    String mota;
    int loai;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTengamoi() {
        return tengamoi;
    }

    public void setTengamoi(String tengamoi) {
        this.tengamoi = tengamoi;
    }

    public String getHinhanh() {
        return hinhanh;
    }

    public void setHinhanh(String hinhanh) {
        this.hinhanh = hinhanh;
    }

    public String getGiaga() {
        return giaga;
    }

    public void setGiaga(String giaga) {
        this.giaga = giaga;
    }

    public String getMota() {
        return mota;
    }

    public void setMota(String mota) {
        this.mota = mota;
    }

    public int getLoai() {
        return loai;
    }

    public void setLoai(int loai) {
        this.loai = loai;
    }
}
